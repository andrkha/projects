USE Общежитие;
GO

-- 1. Индекс для быстрого поиска студентов по ФИО
CREATE INDEX idx_студенты_фио ON студенты (фамилия, имя);

-- 2. Уникальный индекс для номеров комнат в общежитиях
CREATE UNIQUE INDEX idx_комнаты_уникальные ON комнаты (номер, общежитие_id);

-- 3. Индекс для льгот без даты окончания (бессрочные льготы)
CREATE INDEX idx_льготы_без_окончания ON льготы (студент_id, льготная_категория_id) 
WHERE дата_окончания IS NULL;

-- 4. Индекс для льгот с датой окончания в будущем (нужно использовать GETDATE() в переменной)
DECLARE @currentDate DATE = GETDATE();
EXEC('CREATE INDEX idx_льготы_будущие ON льготы (студент_id, льготная_категория_id) 
WHERE дата_окончания >= ''' + @currentDate + '''');

-- 5. Индекс для быстрого расчета стоимости с учетом льгот
CREATE INDEX idx_льготные_категории_скидки ON льготные_категории (коэффициент_скидки, название);

-- 6. Индекс для договоров с указанной датой окончания
CREATE INDEX idx_договоры_с_окончанием ON договоры (дата_начала, дата_окончания) 
WHERE дата_окончания IS NOT NULL;

-- 7. Индекс для договоров без даты окончания (бессрочные)
CREATE INDEX idx_договоры_бессрочные ON договоры (студент_id) 
WHERE дата_окончания IS NULL;

-- 8. Индекс для договоров с датой окончания в будущем (аналогично льготам)
DECLARE @currentDate2 DATE = GETDATE();
EXEC('CREATE INDEX idx_договоры_активные ON договоры (студент_id) 
WHERE дата_окончания >= ''' + @currentDate2 + '''');

-- 9. Индекс для отчетов по оплате
CREATE INDEX idx_квитанции_отчеты ON квитанции (месяц_оплаты, сумма);

-- 10. Индекс для просроченных платежей
CREATE INDEX idx_квитанции_просрочки ON квитанции (договор_id) 
WHERE дата_просрочки IS NOT NULL;

-- 11. Индекс для непогашенных задолженностей (разделил на два индекса)
CREATE INDEX idx_задолженности_непогашенные ON задолженности (договор_id) 
WHERE статус = 'непогашенная';

CREATE INDEX idx_задолженности_частично ON задолженности (договор_id) 
WHERE статус = 'частично погашенная';

-- 12. Составной индекс для поиска комнат
CREATE INDEX idx_комнаты_поиск ON комнаты (вместимость, этаж, стоимость);