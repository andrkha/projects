CREATE VIEW vw_подбор_комнат AS
SELECT 
    к.комната_id,
    о.номер AS общежитие,
    к.номер AS комната,
    к.этаж,
    к.вместимость,
    к.стоимость,
    к.вместимость - COUNT(д.договор_id) AS свободные_места,
    STRING_AGG(лк.название, ', ') WITHIN GROUP (ORDER BY лк.название) AS доступные_льготы
FROM 
    комнаты к
    JOIN общежития о ON к.общежитие_id = о.общежитие_id
    LEFT JOIN договоры д ON к.комната_id = д.комната_id AND 
                          (д.дата_окончания IS NULL OR д.дата_окончания >= GETDATE())
    LEFT JOIN студенты с ON д.студент_id = с.студент_id
    LEFT JOIN льготы л ON с.студент_id = л.студент_id AND 
                         (л.дата_окончания IS NULL OR л.дата_окончания >= GETDATE())
    LEFT JOIN льготные_категории лк ON л.льготная_категория_id = лк.льготная_категория_id
GROUP BY 
    к.комната_id, о.номер, к.номер, к.этаж, к.вместимость, к.стоимость
HAVING 
    к.вместимость - COUNT(д.договор_id) > 0;