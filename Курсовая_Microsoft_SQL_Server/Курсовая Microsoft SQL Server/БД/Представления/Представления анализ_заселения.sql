CREATE VIEW vw_анализ_заселения AS
SELECT 
    о.номер AS общежитие,
    к.номер AS комната,
    к.вместимость,
    COUNT(д.договор_id) AS текущее_количество,
    к.вместимость - COUNT(д.договор_id) AS свободные_места,
    AVG(к.стоимость) AS средняя_стоимость,
    STRING_AGG(с.фамилия + ' ' + с.имя, ', ') AS жильцы
FROM 
    комнаты к
    JOIN общежития о ON к.общежитие_id = о.общежитие_id
    LEFT JOIN договоры д ON к.комната_id = д.комната_id AND 
                          (д.дата_окончания IS NULL OR д.дата_окончания >= GETDATE())
    LEFT JOIN студенты с ON д.студент_id = с.студент_id
GROUP BY 
    о.номер, к.номер, к.вместимость;